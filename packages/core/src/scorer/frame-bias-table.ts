/**
 * コースレベルの枠順バイアス定数テーブル(仕様の枠2層のうち①)。
 *
 * 仕様「枠順バイアス(コースごとの内外有利データを定数テーブルで保持)」および
 * 「枠の影響は2層で独立に評価する: ①コースレベルの枠順バイアス(基礎スコア側の定数テーブル。
 * コース形態によって全馬に効く)、②馬個別の枠別成績(bias-frame.ts)」に対応する。
 *
 * ここでは競馬場 × コース種別 → 内/中/外ゾーンの有利不利(複勝率スケールの加算値)を保持する。
 * 値はいずれも「一般に知られたコース傾向」の概算(±0.1以内)で、チューニング対象
 * (verifyの寄与度ログを見て調整する)。全場・全コースを網羅する必要はなく、傾向が
 * 顕著な場のみ登録し、未登録の条件はデフォルト0(=枠による全体補正なし)とする。
 *
 * 登録の根拠(概算・小回りや急坂で前・内が残りやすい傾向を内枠プラスとして表現):
 *  - 中山(芝/ダ): 小回り・急坂で内前有利とされる。
 *  - 函館・札幌(芝): 洋芝小回りで前・内有利とされる。
 *  - 福島(芝): 小回り平坦で内前有利とされる。
 * 直線が長くフラットな東京・新潟・京都(外回り)などは枠の全体傾向が小さいため未登録(=0)。
 *
 * 将来の拡張(未対応): 枠順傾向は本来「距離」にも依存する(例: 中山ダ1200mはスタート直後にコーナーが
 *   来るため外枠不利=内枠有利が顕著、中山芝2500mは枠差が小さい等)。現状は競馬場×コース種別のみで
 *   距離差を持たない。距離帯別の細分化は verify のキャリブレーション結果を見てから拡張する。
 */

import type { CourseType } from "../scraper/types.js";
import type { FrameZone } from "./derive-features.js";

/** 1コース分の枠ゾーン別バイアス値(内/中/外)。複勝率スケールの加算値。 */
export type CourseFrameBiasRow = Record<FrameZone, number>;

/**
 * 競馬場 × コース種別 → 枠ゾーン別バイアス(概算・チューニング対象)。
 * 未登録の場・コース種別はデフォルト0として扱う(courseFrameBiasValue 参照)。
 */
export const COURSE_FRAME_BIAS_TABLE: Record<
  string,
  Partial<Record<CourseType, CourseFrameBiasRow>>
> = {
  中山: {
    芝: { 内: 0.03, 中: 0, 外: -0.03 },
    ダ: { 内: 0.02, 中: 0, 外: -0.02 },
  },
  函館: {
    芝: { 内: 0.03, 中: 0, 外: -0.03 },
  },
  札幌: {
    芝: { 内: 0.02, 中: 0, 外: -0.02 },
  },
  福島: {
    芝: { 内: 0.02, 中: 0, 外: -0.02 },
  },
};

/**
 * 競馬場・コース種別・枠ゾーンからコースレベルの枠順バイアス値を返す。
 * 未登録(顕著な傾向のない場・コース種別)の場合は0(枠による全体補正なし)。
 */
export function courseFrameBiasValue(
  venueName: string,
  courseType: CourseType,
  zone: FrameZone,
): number {
  const row = COURSE_FRAME_BIAS_TABLE[venueName]?.[courseType];
  if (row === undefined) {
    return 0;
  }
  return row[zone];
}
